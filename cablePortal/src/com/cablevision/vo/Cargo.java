package com.cablevision.vo;

import java.io.Serializable;

public class Cargo implements Serializable{
	
	private static final long serialVersionUID = 1L;
	String descripcionCargo;
	String fechaCargo;
	String importeCargo;
	
	public Cargo(String descripcionCargo, String fechaCargo, String importeCargo){
		this.descripcionCargo = descripcionCargo;
		this.fechaCargo = fechaCargo;
		this.importeCargo = importeCargo;
	}
	
	public String getDescripcionCargo() {
		return descripcionCargo;
	}
	public void setDescripcionCargo(String descripcionCargo) {
		this.descripcionCargo = descripcionCargo;
	}
	public String getFechaCargo() {
		return fechaCargo;
	}
	public void setFechaCargo(String fechaCargo) {
		this.fechaCargo = fechaCargo;
	}
	public String getImporteCargo() {
		return importeCargo;
	}
	public void setImporteCargo(String importeCargo) {
		this.importeCargo = importeCargo;
	}
}
